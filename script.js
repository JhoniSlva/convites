let selectedGuest =
convidados[0];


function renderGuests(filter=''){

guestList.innerHTML = '';

convidados
.filter(convidado=>
convidado.nome
.toLowerCase()
.includes(
filter.toLowerCase()
)

)
.forEach(convidado=>{

const div =
document.createElement('div');

div.className =
'guest-item';
if(convidado.nome === selectedGuest.nome){

div.classList.add(
'active'
);

}

div.innerHTML = `

<div class="guest-header">

<span>
${convidado.nome}
</span>

<small>
${convidado.grupo}
</small>

</div>

${
convidado.acompanhantes > 0

?

`<div class="guest-badge">
+${convidado.acompanhantes}
</div>`

:

''

}
`;

div.onclick = ()=>{

selectedGuest = convidado;

renderGuests(
searchInput.value
);

updatePreview();

};

guestList.appendChild(div);

});

}

renderGuests();

searchInput.addEventListener(
'input',
e=>{

renderGuests(
e.target.value
);

}
);


function updatePreview(){

let finalName =

manualGuest.value.trim()

?

manualGuest.value.trim()

:

selectedGuest.nome;
if(
selectedGuest.acompanhantes > 0
){

inviteType.value =
'casal';

partnerField.classList.remove(
'hidden'
);

}

if(
inviteType.value ===
'casal'
){

if(
partnerName.value.trim()
){

finalName =
`${selectedGuest.nome} e ${partnerName.value}`;

}

}

if(
inviteType.value ===
'familia'
){

finalName =
`${selectedGuest.nome} e Família`;

}

if(
inviteType.value ===
'personalizado'
){

if(
customText.value.trim()
){

finalName =
customText.value;

}

}

previewGuestName.innerText =
finalName.toUpperCase();

}

inviteType.addEventListener(
'change',
()=>{

partnerField.classList.add(
'hidden'
);

customField.classList.add(
'hidden'
);

if(
inviteType.value ===
'casal'
){

partnerField.classList.remove(
'hidden'
);

}

if(
inviteType.value ===
'personalizado'
){

customField.classList.remove(
'hidden'
);

}

updatePreview();

}
);

partnerName.addEventListener(
'input',
updatePreview
);
manualGuest.addEventListener(
'input',
updatePreview
);
customText.addEventListener(
'input',
updatePreview
);

updatePreview();


document
.getElementById(
'downloadBtn'
)
.onclick = async ()=>{

const isMobile =
window.innerWidth < 700;

const canvas =
await html2canvas(
inviteCard,
{

scale:
isMobile ? 1 : 2,

useCORS:true,

backgroundColor:null,

logging:false

}
);
const imgData =
canvas.toDataURL(
'image/png'
);

const { jsPDF } =
window.jspdf;

const pdf =
new jsPDF({

orientation:'portrait',

unit:'px',

format:'a4'

});
const pageWidth =
pdf.internal.pageSize.getWidth();

const ratio =
canvas.height / canvas.width;

const pdfWidth =
pageWidth;

const pdfHeight =
pdfWidth * ratio;

const imageX = 0;
const imageY = 0;

pdf.addImage(

imgData,

'PNG',

imageX,

imageY,

pdfWidth,

pdfHeight

);
const giftLink =
document.getElementById(
'giftLink'
);

const cardRect =
inviteCard.getBoundingClientRect();

const linkRect =
giftLink.getBoundingClientRect();

const scaleX =
pdfWidth / cardRect.width;

const scaleY =
pdfHeight / cardRect.height;

const x =
(linkRect.left - cardRect.left)
* scaleX;

const y =
(linkRect.top - cardRect.top)
* scaleY;

const w =
linkRect.width * scaleX;

const h =
linkRect.height * scaleY;

pdf.link(
x,
y,
w,
h,
{
url:
'https://jhonieveronicanosso.netlify.app/'
}
);
const nome =
previewGuestName
.innerText
.replaceAll(' ','-')
.toLowerCase();

pdf.save(
`convite-${nome}.pdf`
);

};